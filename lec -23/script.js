export function add (num1,num2){
    return num1+num2;
} 

export function subs (num1,num2){
    return num1-num2;
} 

export function mu1(num1,num2) {
    return num1*num2;
} 

export function div(num1,num2){
    return num1/num2;
} 

export function ModDiv(num1,num2){
    return num1%num2;
} 

